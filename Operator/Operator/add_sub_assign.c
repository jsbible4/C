#include <stdio.h>
int main(void)
{
    int num1 = 1;
    int num2 = 1;
    
    num1 += 2;      // num1 = num1 + 2
    num2 -= 2;      // num2 = num2 - 2
    
    printf("%d\n", num1);
    printf("%d\n", num2);
    return 0;
    
}
